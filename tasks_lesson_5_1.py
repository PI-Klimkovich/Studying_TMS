# Напишите программу, которая считывает три числа и подсчитывает сумму только
# положительных чисел
number_1 = int(input("Число 1: "))
number_2 = int(input("Число 2: "))
number_3 = int(input("Число 3: "))
summa = 0
if number_1 > 0:
    summa += number_1
if number_2 > 0:
    summa += number_2
if number_3 > 0:
    summa += number_3
print("Сумма положительных чисел равна", summa)
print()

# иное решение
numbers = [int(input("Число 1: ")), int(input("Число 2: ")), int(input("Число 3: "))]
i, summa = 0, 0
while i < 3:
    if numbers[i] > 0: summa += numbers[i]
    i +=1
print(f"Сумма положительных чисел равна {summa}")
print()


print("lesson 5 - Високосный год")
# Напишите программу, которая определяет, является ли год с данным номером високосным.
# Если год является високосным, то выведите «Да», иначе выведите «Нет».
yar = int(input())
if yar % 4 == 0 and yar % 100 != 0 or yar % 400 == 0:
    print('Да')
else:
    print('Нет')
print()


