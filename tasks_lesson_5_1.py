print("lesson 5 - По середине")
# Написать программу, которая будет принимать на вход строку и выводить её в следующем виде:
# Текст должен располагаться
# по средине
# Длина шапки всегда равна 30 символов, если предложение слишком длинное, то обрезать его.
text = input()

line = '=' * 30
if len(text) > 30:
    print(line, text[:30], line, sep='\n')
else:
    print(line, f"{text:^30}", line, sep='\n')
print()

# иное решение
out_text = f"""
{line}
{text[:30]:^30}
{line}
"""
print(out_text)
print()


